void display_status(std::vector<char> incorrect, std::string answer)
{
  std::cout << "\nIncorrect Guesses: ";
  for(char i: incorrect)
    std::cout << i << " ";
  std::cout << "\n\n";
  std::cout << "Codeword:\n";

  for(char i: answer)
    std::cout << i << " ";
  std::cout << "\n\n";

}


